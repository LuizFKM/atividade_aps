private void configCampos(){
    ConfiguraCampos cc = new ConfiguraCampos();
    
    cc.configCampoText(txtNomeCompleto, "Nome completo");
    cc.configCampoText(txtEmail, "Email");
    cc.configCampoSenha(psfSenha, "Crie uma senha");
    cc.configCampoSenha(psfConfirmarSenha, "Confirme sua senha");
    
}

private Usuario retornaUsuario(){
    String nomeCompleto = txtNomeCompleto.getText();
    String email = txtEmail.getText();
    String senha = String.valueOf(psfSenha.getText());
    String funcao;
    
    if(rdbDiretor.isSelected()){
        funcao = "Diretor";
    } else if (rdbSecretario.isSelected()){
        funcao = "Secretario";
    }else {
        funcao = "Professor";
    }
    
    Usuario usuario = new Usuario();
    
    usuario.setEmail(email);
    usuario.setFuncao(funcao);
    usuario.setNomeCompleto(nomeCompleto);
    usuario.setSenha(senha);
    
    
    
    
    return usuario;
}


private boolean validacaoDosCampos(){

    boolean resultado = true;
    ValidaCampos valida = new ValidaCampos();
    
    if(valida.validaTextField(txtEmail, melhorEmail, lblAviso)) resultado = false;
    if(valida.validaTextField(txtNomeCompleto, firstName, lblAviso)) resultado = false;
    if(valida.validaPasswordField(psfSenha, senha, lblAvisoSenha)) resultado = false;
    if(valida.validaPasswordField(psfConfirmarSenha, confirmarSenha, lblAviso)) resultado = false;
    
    if(!String.valueOf(psfSenha.getPassword()).equals(String.valueOf(psfConfirmarSenha.getPassword()))) {
        psfConfirmarSenha.setBorder(new LineBorder(Color.RED, 2));
        lblAvisoSenha.setText("Senhas n√£o coincidem");
        lblAvisoSenha.setForeground(Color.RED);
        resultado = false;
    }
    
    
    
    
    
    return resultado;
}

